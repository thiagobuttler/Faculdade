    private void atualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarButtonActionPerformed
       int escolha = JOptionPane.showConfirmDialog(null, "Atualizar Item?");
       if (escolha == JOptionPane.YES_OPTION){
         try{
           int id = Integer.parseInt(campoId.getText());
           String nome = campoNome.getText();
           String descricao = campoDescricao.getText();
           Item item = new Item(id, nome, descricao);
           DAO dao = new DAO();
           dao.atualizarItem(item);
           JOptionPane.showMessageDialog(null, "Item atualizado com sucesso");
           buscarItens();
           campoId.setText("0");
           campoNome.setText("");
           campoDescricao.setText("");
         }
         catch(Exception e){
           JOptionPane.showMessageDialog(null, "Falha t√©cnica. Tente novamente mais tarde");
           e.printStackTrace();
         }
       }
    }//GEN-LAST:event_atualizarButtonActionPerformed

    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
        int escolha = JOptionPane.showConfirmDialog(null, "Remover item?");
        if(escolha == JOptionPane.YES_OPTION){
           try{
           int id = Integer.parseInt(campoId.getText());
           Item item = new Item(id);
           DAO dao = new DAO();
           dao.removerItem(item);
           JOptionPane.showMessageDialog(null, "Item removido com sucesso");
           buscarItens();
           campoNome.setText("");
           campoDescricao.setText("");
           campoId.setText("0");
           }
           catch(Exception e){
               JOptionPane.showMessageDialog(null, "Falha t√©cnica. Tente novamente mais tarde");
               e.printStackTrace();
           }
        }
    }//GEN-LAST:event_removerButtonActionPerformed

